/* JavaScript (JS) is a high-level, interpreted programming language primarily used to add interactivity to web pages. It is a dynamic, weakly-typed language with first-class functions and Prototype based OOP feature. While initially designed for client-side scripting in browsers, it is now also widely used on the server-side (Node.js) and in other environments. */

/* History of JavaScript
1995: 
    Brendan Eich, working at Netscape Communications, created JavaScript (initially named Mocha, then LiveScript) in just 10 days. It was designed to be a scripting language for Netscape Navigator 2.0 browser, complementing Java.

Goal:
    To make web pages more interactive and dynamic on the client-side.
    Naming: The name "JavaScript" was a marketing strategy to capitalize on the popularity of Java at the time, although the two languages are fundamentally different.

Early Adoption: 
    JavaScript quickly became essential for web development, allowing for features like form validation, animations, and dynamic content updates without server round trips.

Standardization: 
    To ensure interoperability across different browsers, JavaScript was standardized under the name ECMAScript. The first edition of the ECMAScript standard (ECMA-262) was published in June 1997.

Evolution: 
    Over the years, ECMAScript has seen several significant revisions (ES2015, also known as ES6, was a major update), introducing new features like classes, modules, arrow functions, let and const, Promises, and more.

Server-Side with Node.js: 
    In 2009, Ryan Dahl created Node.js, a JavaScript runtime environment built on Chrome's V8 JavaScript engine. This allowed developers to use JavaScript for server-side programming, opening up new possibilities for full-stack development.

Modern JavaScript: 
    Today, JavaScript is one of the most popular and widely used programming languages in the world, powering everything from front-end web development to back-end servers, mobile applications (React Native), desktop applications (Electron), and even IoT devices. Its ecosystem is vast and constantly evolving with numerous frameworks and libraries. 
    
*/